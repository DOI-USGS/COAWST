!-----------------------------------------------------------------------------
      j = j + 1
      jlabel(j) = 'o3 -> o2 + o(1d)'
      j = j + 1
      jlabel(j) = 'o3 -> o2 + o(3p)'

level_loop : &
      do i = 1,nz
         tinv = 1./tlev(i)
         tl = tlev(i) < 263._dp
