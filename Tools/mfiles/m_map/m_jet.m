function jet2=m_jet(m,ncol)
% M_JET  A perceptually uniform jet
%   M_JET(M) returns an M-by-3 matrix containing the M_jet colormap, a 
%   percentually uniform variation of the JET colormap. It contains the
%   multiple colours which make JET useful, while avoiding the weird
%   highlighting especially around yellow and cyan. The colors begin 
%   with dark blue, range through shades of blue, green, orange and red, 
%   and ends with dark red. M_JET, by itself, is the same length as 
%   the current figure's colormap. If no figure exists, MATLAB uses the 
%   length of the default colormap.
%
%   M_JET('step') returns a 256-color map in which colours are
%   perceptually bunched into 16 separate colours. This is useful 
%   if you want to see "edges" in what would be an otherwise
%   smooth gradation (i.e. approaching the look of contouring). 
%
%   M_JET('demo') Gives a demo of this behavior
%  
%   M_JET('step',M) bunches into M colours.
%
%   M_JET was designed using the CET Perceptually Uniform Colour Maps
%   toolbox at http://peterkovesi.com/projects/colourmaps/
%
%   See also PARULA, HSV, HOT, PINK, FLAG, COLORMAP, RGBPLOT.

% R Pawlowicz Nov/2017

if nargin < 1
   f = get(groot,'CurrentFigure');
   if isempty(f)
      m = size(get(groot,'DefaultFigureColormap'),1);
   else
      m = size(f.Colormap,1);
   end
   ncol=m;
else 
  if isnumeric(m),
    ncol=m;
  else
    switch m(1:3),
      case 'dem'   % demo
         clf;
         subplot(2,1,1);
         imagesc(peaks(10*10));
         title('Peaks function displayed with IMAGESC');
         subplot(2,1,2);
         imagesc(1:256);
	 jet2=m_jet('step');
         colormap(jet2);
         title('The colormap used above: m_jet(256,16)');
	 if nargout==0
	   clear jet2
	 end  
         return;

      case 'ste'    % step
         m=256;
         if nargin<2
	   ncol=16;
	 end  
     otherwise
       error('Unknown option for m_jet');
     end
   end    
end
   
% Whatever length colormap we have
 
if ncol<m/2,
  xx=[0:(m-1)]'/(m-1)*pi*ncol;
  yy=cumsum(cos(xx).^4 + 1/30);
  yy=yy-yy(1);
  yy=yy/max(yy)*255;
else
  yy=[0:(m-1)]'/(m-1)*255;
end;
  
if nargout>0
  jet2=[interp1(0:255,myjet(1),yy) interp1(0:255,myjet(2),yy) interp1(0:255,myjet(3),yy) ];
end



end



