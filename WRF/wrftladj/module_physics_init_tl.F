    REAL, INTENT(OUT) :: pptop
    TYPE(GRID_CONFIG_REC_TYPE) :: config_flags
    LOGICAL, INTENT(IN) :: allowed_to_read
! Local
    REAL :: r, g, ts, gamma, ps, ztrop, tstrat, ptrop, z, t, p, ztop, &
&    ptop
    INTEGER :: k
    REAL :: pwx1
    REAL :: pwy1
    REAL :: pwr1
    REAL :: arg1
    INTRINSIC EXP
    IF (zf(kde/2) .GT. 1.0) THEN
! Height levels assumed (zeta coordinate)
! Convert to sigma using standard atmosphere for pressure-height relation
! constants for standard atmosphere definition
      r = 287.05
      g = 9.80665
      ts = 288.15
      gamma = -(6.5/1000.)
      ps = 1013.25
      ztrop = 11000.
      tstrat = ts + gamma*ztrop
      pwx1 = tstrat/ts
      pwy1 = -(g/(gamma*r))
      pwr1 = pwx1**pwy1
      ptrop = ps*pwr1
      DO k=kde,kds,-1
! full levels
        z = zf(k)
        IF (z .LE. ztrop) THEN
          t = ts + gamma*z
          pwx1 = t/ts
          pwy1 = -(g/(gamma*r))
          pwr1 = pwx1**pwy1
          p = ps*pwr1
        ELSE
          t = tstrat
          arg1 = -(g*(z-ztrop)/(r*tstrat))
          p = ptrop*EXP(arg1)
        END IF
        IF (k .EQ. kde) THEN
          ztop = zf(k)
          ptop = p
        END IF
        sf(k) = (p-ptop)/(ps-ptop)
! half levels
        IF (k .NE. kds) THEN
          z = 0.5*(zf(k)+zf(k-1))
          IF (z .LE. ztrop) THEN
            t = ts + gamma*z
            pwx1 = t/ts
            pwy1 = -(g/(gamma*r))
            pwr1 = pwx1**pwy1
            p = ps*pwr1
          ELSE
            t = tstrat
            arg1 = -(g*(z-ztrop)/(r*tstrat))
            p = ptrop*EXP(arg1)
          END IF
          sh(k-1) = (p-ptop)/(ps-ptop)
        END IF
      END DO
      pptop = ptop/10.
    ELSE
!  Levels are already sigma/eta
      DO k=kde,kds,-1
!        sf(k)=zf(kde-k+kds)
!        if(k .ne. kde)sh(k)=zh(kde-1-k+kds)
        sf(k) = zf(k)
        IF (k .NE. kde) sh(k) = zh(k)
      END DO
      pptop = p_top/1000.
    END IF
  END SUBROUTINE Z2SIGMA
END MODULE G_MODULE_PHYSICS_INIT
