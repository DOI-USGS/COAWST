#include "cppdefs.h"
      MODULE tl_set_avg_mod
#if (defined TL_AVERAGES && defined TANGENT) || \
    (defined RP_AVERAGES && defined TL_IOMS)
!
!svn $Id: tl_set_avg.F 995 2020-01-10 04:01:28Z arango $
