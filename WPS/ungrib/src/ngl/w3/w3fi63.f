      INTEGER       KRET
      KRET=0
C  -------------------  PROCESS SECTION 1
      KPTR(8)  = KPTR(9) + KPTR(2) * 8 + 24
C  BYTE 4
C                   PARAMETER TABLE VERSION NR
          CALL GBYTEC (MSGA,KPDS(19),KPTR(8),8)
          KPTR(8)   = KPTR(8) + 8
C  BYTE 5           IDENTIFICATION OF CENTER
      CALL GBYTEC (MSGA,KPDS(1),KPTR(8),8)
      KPTR(8)   = KPTR(8) + 8
